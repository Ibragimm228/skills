export const getSkillsByCategory = (category: string): Skill[] => {
  return skillsData.filter(skill => skill.category === category);
};

export const getSkillsByDifficulty = (difficulty: string): Skill[] => {
  return skillsData.filter(skill => skill.difficulty_level === difficulty);
};

export const getSkillsByTimeRange = (minHours: number, maxHours: number): Skill[] => {
  return skillsData.filter(skill => 
    skill.hours_required >= minHours && skill.hours_required <= maxHours
  );
};

export const getCategories = (): string[] => {
  return [...new Set(skillsData.map(skill => skill.category))].sort();
};
